    public VentanaIngresos() {
        initComponents();
        setLocationRelativeTo(null);
        // Preparar la tabla
        DefaultTableModel tm = new DefaultTableModel();
        tableIngresos.setModel(tm);
        
        // Conexión con la DB
        Conexion conexion = new Conexion();
        Connection con = conexion.getConexion();
        
        // Ejecución de la consulta
        PreparedStatement ps = null;
        ResultSet rs = null;
        String sql = "SELECT * FROM ingresos";
        try{
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int cantidadColumnas = rsmd.getColumnCount();
            tm.addColumn("Ticket");
            tm.addColumn("Nombre propietario");
            tm.addColumn("DNI propietario");
            tm.addColumn("Monto cobrado");
            tm.addColumn("Fecha salida");
            // Recorre la tabla SQL y agrega las filas a jTable
            while(rs.next()){
                Object[] filas = new Object[cantidadColumnas];
                for(int i=0; i<cantidadColumnas; i++){
                    filas[i] = rs.getObject(i+1);
                }
            tm.addRow(filas);
            }
        } catch(SQLException e) {
            System.err.println(e);
        }
    }

